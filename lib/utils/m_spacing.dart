import 'package:flutter/material.dart';

/// Espaçamento vertical com altura customizável (padrão: 12.0)
SizedBox mHeight([double height = 12.0]) {
  return SizedBox(height: height);
}

/// Espaçamento horizontal com largura customizável (padrão: 12.0)
SizedBox mWidth([double width = 12.0]) {
  return SizedBox(width: width);
}
